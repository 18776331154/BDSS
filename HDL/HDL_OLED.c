u8 OLED_GRAM[128][8];	   
void HDL_OLED_REFRESH_GRAM(void) //刷新显示
{
	u8 i,n;		    
	for(i=0;i<8;i++)  
	{  
		HDL_OLED_WR_BYTE (0xb0+i,OLED_CMD);    //设置页地址（0~7）
		HDL_OLED_WR_BYTE (0x00,OLED_CMD);      //设置显示位置―列低地址
		HDL_OLED_WR_BYTE (0x10,OLED_CMD);      //设置显示位置―列高地址   
		for(n=0;n<128;n++)HDL_OLED_WR_BYTE(OLED_GRAM[n][i],OLED_DATA); 
	}   
}
/**************************************************************************
函数功能：向OLED写入一个字节
入口参数：dat:要写入的数据/命令  ，cmd:数据/命令标志 0,表示命令;1,表示数据;
返 回 值：无
**************************************************************************/
void HDL_OLED_WR_BYTE(u8 dat,u8 cmd)
{	
	u8 i;			  
	if(cmd)
	  OLED_RS_Set();
	else 
	  OLED_RS_Clr();		  
	for(i=0;i<8;i++)
	{			  
		OLED_SCLK_Clr();
		if(dat&0x80)
		   OLED_SDIN_Set();
		else 
		   OLED_SDIN_Clr();
		OLED_SCLK_Set();
		dat<<=1;   
	}				 		  
	OLED_RS_Set();   	  
} 
