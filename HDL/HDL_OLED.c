//清屏函数,清完屏,整个屏幕是黑色的!和没点亮一样!!!	  
void HDL_OLED_CLEAR(void)  
{  
	u8 i,n;  
	for(i=0;i<8;i++)for(n=0;n<128;n++)OLED_GRAM[n][i]=0X00;  
	HDL_OLED_REFRESH_GRAM();//更新显示
}
/**************************************************************************
函数功能：在OLED中画点
入口参数：坐标:x：0~127，y:0~63  ，t:1 填充 0,清空		
返 回 值：无
**************************************************************************/
void HDL_OLED_DRAW_POINT(u8 x,u8 y,u8 t)
{
	u8 pos,bx,temp=0;
	if(x>127||y>63)return;//超出范围了.
	pos=7-y/8;
	bx=y%8;
	temp=1<<(7-bx);
	if(t)OLED_GRAM[x][pos]|=temp;
	else OLED_GRAM[x][pos]&=~temp;	    
}
/**************************************************************************
函数功能：在指定的位置显示一个字符
入口参数：坐标:x：0~127，y:0~63 ，mode:0,反白显示;1,正常显示	，size:选择字体 12-16		
返 回 值：无
**************************************************************************/
void HDL_OLED_SHOWCHAR(u8 x,u8 y,u8 chr,u8 size,u8 mode)
{      			    
	u8 temp,t,t1;
	u8 y0=y;
	chr=chr-' ';//得到偏移后的值				   
    for(t=0;t<size;t++)
    {   
		if(size==12)temp=oled_asc2_1206[chr][t];  //调用1206字体
		else temp=oled_asc2_1608[chr][t];		 //调用1608字体 	                          
        for(t1=0;t1<8;t1++)
		{
			if(temp&0x80)HDL_OLED_DRAW_POINT(x,y,mode);
			else HDL_OLED_DRAW_POINT(x,y,!mode);
			temp<<=1;
			y++;
			if((y-y0)==size)
			{
				y=y0;
				x++;
				break;
			}
		}  	 
    }          
}

//m^n函数
u32 oled_pow(u8 m,u8 n)
{
	u32 result=1;	 
	while(n--)result*=m;    
	return result;
}	
/**************************************************************************
函数功能：显示2个数字
入口参数：坐标:x：0~127，y:0~63 ；num:数值(0~4294967295)；mode:0,填充模式 1,叠加模式； size:选择字体 12-16		
返 回 值：无
**************************************************************************/  
void HDL_OLED_SHOWNUMBER(u8 x,u8 y,u32 num,u8 len,u8 size)
{         	
	u8 t,temp;
	u8 enshow=0;						   
	for(t=0;t<len;t++)
	{
		temp=(num/oled_pow(10,len-t-1))%10;
		if(enshow==0&&t<(len-1))
		{
			if(temp==0)
			{
				HDL_OLED_SHOWCHAR(x+(size/2)*t,y,' ',size,1);
				continue;
			}else enshow=1; 
		 	 
		}
	 	HDL_OLED_SHOWCHAR(x+(size/2)*t,y,temp+'0',size,1); 
	}
} 
/**************************************************************************
函数功能：显示字符串
入口参数：坐标:x：0~127，y:0~63 ；*p:字符串起始地址；
返 回 值：无
**************************************************************************/  
void HDL_OLED_SHOWSTRING(u8 x,u8 y,const u8 *p)
{
#define MAX_CHAR_POSX 122
#define MAX_CHAR_POSY 58          
    while(*p!='\0')
    {       
        if(x>MAX_CHAR_POSX){x=0;y+=16;}
        if(y>MAX_CHAR_POSY){y=x=0;HDL_OLED_CLEAR();}
        HDL_OLED_SHOWCHAR(x,y,*p,12,1);	 
        x+=8;
        p++;
    }  
}	
/**************************************************************************
函数功能：初始化OLED
入口参数：无
返 回 值：无
**************************************************************************/ 
void HDL_OLED_INIT(void)
{ 	 	 
  GPIO_InitTypeDef GPIO_InitStruct;
                                                      //使能GPIOA GPIOB时钟
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA|RCC_APB2Periph_GPIOB,ENABLE); 
	
	GPIO_InitStruct.GPIO_Mode = GPIO_Mode_Out_PP;        //PB3 4 5推挽输出
	GPIO_InitStruct.GPIO_Pin = GPIO_Pin_3|GPIO_Pin_4|GPIO_Pin_5;
	GPIO_InitStruct.GPIO_Speed = GPIO_Speed_2MHz;
	GPIO_Init(GPIOB,&GPIO_InitStruct);

	GPIO_InitStruct.GPIO_Mode = GPIO_Mode_Out_PP;       //PA15 推挽输出
	GPIO_InitStruct.GPIO_Pin = GPIO_Pin_15;
	GPIO_InitStruct.GPIO_Speed = GPIO_Speed_2MHz;
	GPIO_Init(GPIOA,&GPIO_InitStruct);
		

	OLED_RST_Clr();
	delay_ms(100);
	OLED_RST_Set();   //OLED复位
					  
	HDL_OLED_WR_BYTE(0xAE,OLED_CMD); //关闭显示
	HDL_OLED_WR_BYTE(0xD5,OLED_CMD); //设置时钟分频因子,震荡频率
	HDL_OLED_WR_BYTE(80,OLED_CMD);   //[3:0],分频因子;[7:4],震荡频率
	HDL_OLED_WR_BYTE(0xA8,OLED_CMD); //设置驱动路数
	HDL_OLED_WR_BYTE(0X3F,OLED_CMD); //默认0X3F(1/64) 
	HDL_OLED_WR_BYTE(0xD3,OLED_CMD); //设置显示偏移
	HDL_OLED_WR_BYTE(0X00,OLED_CMD); //默认为0

	HDL_OLED_WR_BYTE(0x40,OLED_CMD); //设置显示开始行 [5:0],行数.
													    
	HDL_OLED_WR_BYTE(0x8D,OLED_CMD); //电荷泵设置
	HDL_OLED_WR_BYTE(0x14,OLED_CMD); //bit2，开启/关闭
	HDL_OLED_WR_BYTE(0x20,OLED_CMD); //设置内存地址模式
	HDL_OLED_WR_BYTE(0x02,OLED_CMD); //[1:0],00，列地址模式;01，行地址模式;10,页地址模式;默认10;
	HDL_OLED_WR_BYTE(0xA1,OLED_CMD); //段重定义设置,bit0:0,0->0;1,0->127;
	HDL_OLED_WR_BYTE(0xC0,OLED_CMD); //设置COM扫描方向;bit3:0,普通模式;1,重定义模式 COM[N-1]->COM0;N:驱动路数
	HDL_OLED_WR_BYTE(0xDA,OLED_CMD); //设置COM硬件引脚配置
	HDL_OLED_WR_BYTE(0x12,OLED_CMD); //[5:4]配置
		 
	HDL_OLED_WR_BYTE(0x81,OLED_CMD); //对比度设置
	HDL_OLED_WR_BYTE(0xEF,OLED_CMD); //1~255;默认0X7F (亮度设置,越大越亮)
	HDL_OLED_WR_BYTE(0xD9,OLED_CMD); //设置预充电周期
	HDL_OLED_WR_BYTE(0xf1,OLED_CMD); //[3:0],PHASE 1;[7:4],PHASE 2;
	HDL_OLED_WR_BYTE(0xDB,OLED_CMD); //设置VCOMH 电压倍率
	HDL_OLED_WR_BYTE(0x30,OLED_CMD); //[6:4] 000,0.65*vcc;001,0.77*vcc;011,0.83*vcc;

	HDL_OLED_WR_BYTE(0xA4,OLED_CMD); //全局显示开启;bit0:1,开启;0,关闭;(白屏/黑屏)
	HDL_OLED_WR_BYTE(0xA6,OLED_CMD); //设置显示方式;bit0:1,反相显示;0,正常显示	    						   
	HDL_OLED_WR_BYTE(0xAF,OLED_CMD); //开启显示	 
	HDL_OLED_CLEAR();                //清屏
}  


